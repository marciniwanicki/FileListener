#pragma mark - Application's Documents directory

/**
 Returns the URL to the application's Documents directory.
 */
- (NSURL *)applicationDocumentsDirectory
{
    return [[[NSFileManager defaultManager] URLsForDirectory:NSDocumentDirectory inDomains:NSUserDomainMask] lastObject];
}

#pragma mark - Custom code
/**
 Checking remote file.
 */
- (void) checkRemoteFile {
    if (self.fileChecker == nil) {
        self.fileChecker = [[FileChecker alloc] init];
    }
    NSError *error;
    if ([self.fileChecker isFileChanged: &error]) {
        NSString *remoteFileUrl = [[self.fileChecker loadUrlFromFile: &error] absoluteString];
        if (remoteFileUrl == nil) {
            return;
        }
        UILocalNotification *localNotif = [[UILocalNotification alloc] init];
        if (localNotif) {
            localNotif.fireDate = [NSData data];
            localNotif.alertBody = [NSString stringWithFormat: @"File %@ has changed.", remoteFileUrl];
            localNotif.alertAction = NSLocalizedString(@"Change URL", nil);
            localNotif.soundName = @"alarmsound.caf";
            localNotif.applicationIconBadgeNumber = 1;
                
            [[UIApplication sharedApplication] presentLocalNotificationNow: localNotif];
        }
    }
}


/**
 Checking for background support.
 */
- (BOOL) isBackgroundSupported {
    UIDevice* device = [UIDevice currentDevice];
    BOOL backgroundSupported = NO;
    if ([device respondsToSelector:@selector(isMultitaskingSupported)])
        backgroundSupported = device.multitaskingSupported;
    return backgroundSupported;
}

